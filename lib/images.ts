// Helper function to get optimized image URL
export function getOptimizedImage(url: string, width: number = 800): string {
  // If already has width parameter, replace it
  if (url.includes("w=")) {
    return url.replace(/w=\d+/, `w=${width}`);
  }
  // If URL has query params, add width
  if (url.includes("?")) {
    return `${url}&w=${width}`;
  }
  // Otherwise add width
  return `${url}?w=${width}`;
}

